                                <strong>Lithium batteries connected in series</strong><br>
                                Add the voltage of batteries, capacity remains the same, and internal resistance increases.
                            </p>
                        </li>
                        <li class="section20">
                            <p>
                                <strong>Lithium batteries connected in paralle</strong><br>
                                Constant voltage, added capacity, reduced internal resistance, and extended power supply time.
                            </p>
                        </li>
                        <li class="section20">
                            <p>
                                <strong>Lithium batteries connected in series and parallel</strong><br>
                                3.7V single battery can be assembled into battery pack with a voltage of <strong>3.7*(N)V </strong>as required (N: number of single batteries)<br/>
                                For example, 7.4V, 12V, 24V, 36V, 48V, 60V, 72V, etc.
                            </p>
                        </li>
